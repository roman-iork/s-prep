#include <stdio.h>

// Функция для разворота части массива через указатели
void reverse(int *start, int *end) {
    while (start < end) {
        int temp = *start;
        *start = *end;
        *end = temp;
        start++;
        end--;
    }
}

// Функция циклического сдвига
void cyclic_shift(int *arr, int n, int c) {
    if (n <= 1) return;

    // 1. Нормализуем сдвиг: c % n убирает лишние полные круги.
    // Если сдвиг отрицательный (вправо), например -1 при n=5, 
    // то сдвиг вправо на 1 — это то же самое, что сдвиг влево на 4 (5 + (-1) = 4).
    c = c % n;
    if (c < 0) {
        c = c + n;
    }

    // Если чистый сдвиг равен 0, ничего делать не нужно
    if (c == 0) return;

    // 2. Алгоритм разворота трех частей (работает за O(n) времени и O(1) памяти):
    // Разворачиваем первые c элементов
    reverse(arr, arr + c - 1);
    // Разворачиваем оставшиеся элементы
    reverse(arr + c, arr + n - 1);
    // Разворачиваем весь массив целиком
    reverse(arr, arr + n - 1);
}

int main() {
    int n, c;

    // Считываем размер массива
    if (scanf("%d", &n) != 1 || n <= 0) {
        return 1;
    }

    // Так как stdlib.h (и malloc) запрещен, используем VLA (Variable Length Array)
    int arr[n];

    // Считываем элементы массива через указатель
    for (int i = 0; i < n; i++) {
        scanf("%d", arr + i); // arr + i — это адрес i-го элемента
    }

    // Считываем величину сдвига
    if (scanf("%d", &c) != 1) {
        return 1;
    }

    // Вызываем функцию сдвига, передавая массив как указатель
    cyclic_shift(arr, n, c);

    // Выводим результат с помощью указателей
    for (int i = 0; i < n; i++) {
        printf("%d", *(arr + i)); // Разыменовываем указатель для чтения
        if (i < n - 1) {
            printf(" ");
        }
    }
    printf("\n");

    return 0;
}
